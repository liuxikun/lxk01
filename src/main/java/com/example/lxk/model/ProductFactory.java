package com.example.lxk.model;

public class ProductFactory {
	
	private String id;
	private String productFactoryName;
	private String productFactoryAddr;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getProductFactoryName() {
		return productFactoryName;
	}
	public void setProductFactoryName(String productFactoryName) {
		this.productFactoryName = productFactoryName;
	}
	public String getProductFactoryAddr() {
		return productFactoryAddr;
	}
	public void setProductFactoryAddr(String productFactoryAddr) {
		this.productFactoryAddr = productFactoryAddr;
	}
}
